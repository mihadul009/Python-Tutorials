#    Operators

# Python has different types of operators for different operations. To create a calculator we require arithmetic operators.
# +	 :Addition	      :15+7
# -	 :Subtraction	  :15-7
# *	 :Multiplication  :5*7
# ** :Exponential	  :5**3
# /	 :Division	      :5/3
# %	 :Modulus	      :15%7
# // :Floor Division  :15//7
print(5+6)
print(15-6)
print(15*6)
print(15/6)
print(15//6)
print(5%3)
print(2**4)